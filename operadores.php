<?php
/*Existen diferentes tipos de operadores:
    * Operadores Lógicos
    *Operadores aritméticos
    *Operadores de asignación
    *Operadores de comparación
    */
 //OPERADORES ARITMÉTICOS

    //Definir variable número

    $numero1 = 20;
    $numero2 = 6;

    //Suma
    echo $numero1 + $numero2;
    echo "<br>";

    //Resta
    echo $numero1 - $numero2;
    echo "<br>";

    //Multiplicación
    echo $numero1 * $numero2;
    echo "<br>";

    //División
    echo $numero1 / $numero2;
    echo "<br>";

    //Módulo
    echo $numero1 % $numero2;
    echo "<br>";

    //Incremento
    $numero1++;
    echo $numero1;
    echo "<br>";

    //Decremento
    $numero1--;
    echo $numero1;
    echo "<br>";

    //Potencia
    echo pow($numero1, $numero2);
    echo "<br>";

    //Raíz cuadrada
    echo sqrt($numero1);
    echo "<br>";

    //Valor absoluto
    echo abs($numero1);
    echo "<br>";

    //Redondeo
    echo round($numero1);
    echo "<br>";



